def get_installed_libraries(python_path: str) -> str:
    """Detects installed scientific libraries in the environment (filtered for relevance)."""
    try:
        result = subprocess.run(
            [python_path, "-m", "pip", "list", "--format=json"],
            capture_output=True,
            text=True,
            timeout=15
        )
        if result.returncode == 0:
            packages = json.loads(result.stdout)
            # 仅保留科学计算相关库（减少噪声，避免暴露敏感包）
            scientific_pkgs = [
                p["name"] for p in packages 
                if any(kw in p["name"].lower() for kw in [
                    'numpy', 'scipy', 'scikit-image', 'skimage', 'pillow', 'opencv', 
                    'matplotlib', 'pandas', 'numba', 'torch', 'tensorflow', 'jax'
                ])
            ]
            if scientific_pkgs:
                return ", ".join(sorted(set(scientific_pkgs)))  # 去重排序
            else:
                return "numpy, scipy (minimal scientific stack)"
    except Exception as e:
        return f"numpy, scipy (detection failed: {type(e).__name__})"
